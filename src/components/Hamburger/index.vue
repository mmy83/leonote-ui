<template>
  <div style="padding: 2px 15px; color: #ffffff" @click="toggleClick">
    <i v-if="isActive" class="fa fa-dedent" />
    <i v-if="!isActive" class="fa fa-indent" />
  </div>
</template>

<script>
export default {
  name: 'Hamburger',
  props: {
    isActive: {
      type: Boolean,
      default: false
    }
  },
  methods: {
    toggleClick() {
      this.$emit('toggleClick')
    }
  }
}
</script>

<style scoped>
.hamburger {
  display: inline-block;
  vertical-align: middle;
  width: 20px;
  height: 20px;
}

.hamburger.is-active {
  transform: rotate(180deg);
}
